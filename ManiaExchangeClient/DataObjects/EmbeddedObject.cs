namespace ManiaExchangeClient.DataObjects
{
    public class EmbeddedObject
    {
        /// <summary>
        /// Gets or sets the object path
        /// </summary>
        public string ObjectPath { get; set; }

        /// <summary>
        /// Gets or sets the author of the object
        /// </summary>
        public string ObjectAuthor { get; set; }

        /// <summary>
        /// Gets or sets the external link of the object
        /// </summary>
        public string ExternalLink { get; set; }

        /// <summary>
        /// Gets or sets the name of the object
        /// </summary>
        public string Name { get; set; }
    }
}
